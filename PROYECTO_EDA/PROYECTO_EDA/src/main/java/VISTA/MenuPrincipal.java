    private void btListarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btListarActionPerformed
        // TODO add your handling code here:
        DefaultTableModel tabla = new DefaultTableModel();
        this.tListado.setModel(tabla);
        tabla.addColumn("IDENTIFICADOR");
        tabla.addColumn("PRIORIDAD");
        tabla.addColumn("ASUNTO");
        tabla.addColumn("DOCUMENTO");
        tabla.addColumn("DNI");
        tabla.addColumn("NOMBRE");
        tabla.addColumn("TELEFONO");
        tabla.addColumn("EMAIL");
        
        Expediente aux;
        Nodo actual = gestionExp.getPrimero();
        int contador = 0;
        if(actual != null){
            while(actual != null){
                aux = (Expediente) actual.getElemento();
                tabla.insertRow(contador, new Object[]{
                    aux.getIdentificador(),
                    aux.getPrioridad(),
                    aux.getAsunto(),
                    aux.getDocumento(),
                    aux.getDNI(),
                    aux.getNombre(),
                    aux.getTelefono(),
                    aux.getEmail()
                });
                contador += 1;
                
                actual = actual.getSiguiente();
            }
        }
    }//GEN-LAST:event_btListarActionPerformed

